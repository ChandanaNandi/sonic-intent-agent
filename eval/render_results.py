"""Generate eval/results.md from a harness.py JSON output file.

Reads the JSON written by harness.py and produces a markdown report
suitable for committing alongside the eval harness itself. The report
shows the summary, a per-prompt results table, and a failures section
for any non-PASS results.

Use:
    python3 render_results.py --input /tmp/eval_run.json
    python3 render_results.py --input /tmp/eval_run.json \\
        --output ~/sonic-project/eval/results.md
"""

import argparse
import datetime
import json
import sys
from pathlib import Path


def _truncate(text: str, max_len: int = 60) -> str:
    """Trim a string for table display."""
    if len(text) <= max_len:
        return text
    return text[: max_len - 3] + "..."


def render(payload: dict, run_timestamp: str) -> str:
    """Render the JSON payload as a markdown report."""
    summary = payload["summary"]
    results = payload["results"]

    lines: list[str] = []
    lines.append("Eval results")
    lines.append("")
    lines.append(
        f"Run timestamp: {run_timestamp}"
    )
    lines.append(
        f"Model: qwen2.5:7b-instruct via Ollama"
    )
    lines.append(f"Total prompts: {summary['total']}")
    lines.append(f"Passed: {summary['passed']}")
    lines.append(f"Failed: {summary['failed']}")
    lines.append(
        f"Pass rate: {summary['pass_rate_pct']:.1f}%"
    )
    lines.append(
        f"Total elapsed: {summary['elapsed_seconds']:.1f} seconds"
    )
    lines.append("")
    lines.append(
        "This file is generated by render_results.py from the JSON "
        "output of harness.py."
    )
    lines.append(
        "Re-run the harness and regenerate to refresh."
    )
    lines.append("")

    lines.append("Per-prompt results")
    lines.append("")
    lines.append(
        "| # | Prompt | Expected tool | Actual tool | Verdict | Time |"
    )
    lines.append(
        "|---|--------|---------------|-------------|---------|------|"
    )
    for index, row in enumerate(results, start=1):
        prompt = _truncate(row["prompt"], max_len=55)
        expected = row["expected_tool"]
        actual = row["actual_tool"] or "-"
        verdict = row["verdict"]
        elapsed = f"{row['elapsed_seconds']:.1f}s"
        lines.append(
            f"| {index} | {prompt} | {expected} | {actual} | "
            f"{verdict} | {elapsed} |"
        )

    failures = [r for r in results if r["verdict"] != "PASS"]
    lines.append("")
    lines.append("Failures")
    lines.append("")
    if not failures:
        lines.append("No failures in this run.")
    else:
        for failure in failures:
            lines.append(f"Prompt: {failure['prompt']!r}")
            lines.append(f"  Expected tool: {failure['expected_tool']}")
            lines.append(
                f"  Expected args: {failure['expected_args']}"
            )
            lines.append(f"  Actual tool: {failure['actual_tool'] or '(none)'}")
            lines.append(f"  Actual args: {failure['actual_args']}")
            lines.append(f"  Verdict: {failure['verdict']}")
            lines.append(f"  Reason: {failure['reason']}")
            if failure["raw_text"]:
                lines.append(
                    f"  Raw text fallback: {failure['raw_text'][:200]!r}"
                )
            lines.append("")

    lines.append("")
    lines.append("Methodology")
    lines.append("")
    lines.append(
        "The harness runs agent.py --eval-mode for each prompt in "
        "prompts.py. The agent invokes the LLM once and writes the "
        "structured tool calls as JSON to stdout. No tools are "
        "executed; no SONiC changes are applied; no Batfish "
        "verification runs. The harness measures only LLM tool-call "
        "accuracy."
    )
    lines.append("")
    lines.append(
        "Verdicts: PASS (tool and args match expected), "
        "FAIL_WRONG_TOOL (wrong tool name), FAIL_WRONG_ARGS (right "
        "tool, wrong args), FAIL_TEXT_FALLBACK (LLM emitted text "
        "instead of structured call), FAIL_NO_TOOL_CALL (no calls "
        "and no text), FAIL_SUBPROCESS_ERROR (agent crashed or timed "
        "out)."
    )
    lines.append("")
    lines.append(
        "Argument matching modes: 'exact' requires all expected args "
        "present with exact values and no extras. 'subset' requires "
        "expected args present and matching; extras allowed. The "
        "admin_status prompts use 'subset' because the LLM may pass "
        "additional fields or use synonymous string values."
    )
    lines.append("")

    return "\n".join(lines)


def main() -> int:
    """CLI entry point. Reads harness JSON and writes markdown."""
    parser = argparse.ArgumentParser(
        description="Render harness JSON output as markdown."
    )
    parser.add_argument(
        "--input", required=True,
        help="path to harness.py JSON output",
    )
    parser.add_argument(
        "--output", default="-",
        help="path to write markdown (default: stdout)",
    )
    parser.add_argument(
        "--timestamp", default=None,
        help="run timestamp (default: now in YYYY-MM-DD HH:MM format)",
    )
    args = parser.parse_args()

    try:
        payload = json.loads(Path(args.input).read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError) as exc:
        print(f"error: could not read input: {exc}", file=sys.stderr)
        return 1

    timestamp = args.timestamp or datetime.datetime.now().strftime(
        "%Y-%m-%d %H:%M"
    )

    markdown = render(payload, timestamp)

    if args.output == "-":
        print(markdown)
    else:
        Path(args.output).write_text(markdown, encoding="utf-8")
        print(f"wrote {args.output}", file=sys.stderr)

    return 0


if __name__ == "__main__":
    sys.exit(main())
